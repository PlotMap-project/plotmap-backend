package com.plotmap.backend.model.enum

enum class ConnectionType {
    /** Причинно-следственная: событие A привело к событию B */
    CAUSAL,
    /** Хронологическая: A произошло раньше B, но не является причиной */
    TEMPORAL,
    /** Параллельная: события происходят одновременно */
    PARALLEL,
    /** Противоречие: события логически несовместимы (отображается красным) */
    CONTRADICTION,
}
