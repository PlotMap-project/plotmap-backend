package com.plotmap.backend.model.enum

enum class SystemEventRole {
    /** Завязка — событие, запускающее основной конфликт */
    INCITING_INCIDENT,
    /** Развитие — нарастание напряжения и конфликта */
    RISING_ACTION,
    /** Кульминация — пик напряжения, поворотный момент */
    CLIMAX,
    /** Спад — последствия кульминации */
    FALLING_ACTION,
    /** Развязка — разрешение конфликта */
    RESOLUTION,
    /** Сюжетный поворот — неожиданное изменение событий */
    PLOT_TWIST,
    /** Обычное событие без особой нарративной функции */
    REGULAR
}
